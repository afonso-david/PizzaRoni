A pizza é <p>
A pizza é hoje um dos pratos mais populares do mundo e tem a sua origem em Itália, mais precisamente na cidade de Nápoles.<br><br>

A sua história começa há muitos séculos, quando diferentes povos já tinham o hábito de cozinhar massas de pão achatadas e adicionar ingredientes por cima. Na antiguidade, civilizações como os gregos, egípcios e romanos já preparavam algo semelhante, utilizando pão plano com azeite, ervas aromáticas e outros alimentos simples. Cada cultura adaptava os ingredientes disponíveis localmente, o que fez com que surgissem variações interessantes, algumas muito semelhantes àquilo que viria a ser a pizza moderna.<br><br>

No entanto, a pizza como a conhecemos hoje começou a desenvolver-se muitos séculos depois. Foi em Nápoles, durante os séculos XVII e XVIII, que a pizza começou a ganhar a forma moderna. Nessa época, era um alimento simples e barato, consumido principalmente pelas classes mais pobres da cidade. A massa era feita com farinha, água e fermento, e depois era coberta com ingredientes acessíveis, como tomate, alho, azeite e algumas ervas aromáticas. Estas primeiras pizzas eram vendidas em ruas e praças, tornando-se rapidamente um prato popular entre os habitantes locais.<br><br>

Um momento importante na história da pizza aconteceu em 1889, quando o pizzaiolo Raffaele Esposito criou uma pizza especial para homenagear a rainha Margherita de Itália. Esta pizza foi coberta com tomate, mozzarella e manjericão, cores que representavam a bandeira italiana, e recebeu o nome de <em>Pizza Margherita</em>. Este gesto contribuiu para a fama da pizza e ajudou a consolidar Nápoles como a capital mundial desta iguaria.<br><br>

No início do século XX, com a imigração italiana para diferentes países, especialmente para os Estados Unidos, a pizza começou a espalhar-se pelo mundo. Nos Estados Unidos, surgiram novas versões, como a pizza de Nova Iorque, conhecida pela sua massa fina e esticada, e a pizza estilo Chicago, com massa grossa e recheio abundante. Cada país adaptou a receita às preferências locais, criando variações de sabores e ingredientes que hoje conhecemos globalmente.<br><br>

Além disso, a pizza evoluiu ao longo do tempo em termos de técnicas e criatividade. Hoje, existem milhares de tipos de pizza: desde as tradicionais, como Margherita, Marinara e Quattro Formaggi, até combinações gourmet com ingredientes exóticos, frutos do mar ou até doces. A sua popularidade é tal que existem competições internacionais de pizzaiolos, festivais dedicados e até museus da pizza, celebrando esta criação que começou como uma refeição simples e humilde.<br><br>

A pizza não é apenas comida, mas também um símbolo cultural e social, representando a partilha, a criatividade e a tradição culinária italiana que conquistou o mundo inteiro. O seu sucesso global mostra como algo simples pode transformar-se num ícone universal da gastronomia.
